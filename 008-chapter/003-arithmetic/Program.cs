// Пример сложения
int sumResult = 5 + 3;
Console.WriteLine("Результат сложения: " + sumResult); // Выведет: Результат сложения: 8

// Пример вычитания
int subtractResult = 5 - 3;
Console.WriteLine("Результат вычитания: " + subtractResult); // Выведет: Результат вычитания: 2

// Пример умножения
int multiplyResult = 5 * 3;
Console.WriteLine("Результат умножения: " + multiplyResult); // Выведет: Результат умножения: 15

// Пример деления
double divideResult = 21 / 2;
Console.WriteLine("Результат деления: " + divideResult); // Выведет: Результат деления: 2

// Пример остатка от деления
int remainderResult = 21 % 2;
Console.WriteLine("Остаток от деления: " + remainderResult); // Выведет: Остаток от деления: 1

// Пример изменения приоритета операций с использованием скобок
int priorityResult = (5 + 3) * 2;
Console.WriteLine("Результат с изменением приоритета: " + priorityResult); // Выведет: Результат с изменением приоритета: 16

// Пример работы с числами с плавающей точкой
float f = 1.2f; // Число с плавающей точкой одинарной точности
decimal d = 12.33m; // Десятичное число с высокой точностью
Console.WriteLine("Число float: " + f + ", число decimal: " + d);

// Пример операции инкремента
int i = 0;
Console.WriteLine("Исходное значение i: " + i);
i++; ++i;
Console.WriteLine("Значение i после инкремента: " + i); // Выведет: Значение i после инкремента: 1

// Пример операции декремента
int j = 5;
Console.WriteLine("Исходное значение j: " + j);
j--; --j;
Console.WriteLine("Значение j после декремента: " + j); // Выведет: Значение j после декремента: 4